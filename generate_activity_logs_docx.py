# ── Low-level XML helpers ─────────────────────────────────────────────────────
def set_cell_bg(cell, hex_color):
    tc   = cell._tc
    tcPr = tc.get_or_add_tcPr()
    shd  = OxmlElement("w:shd")
    shd.set(qn("w:val"),   "clear")
    shd.set(qn("w:color"), "auto")
    shd.set(qn("w:fill"),  hex_color.lstrip("#"))
    tcPr.append(shd)

def set_cell_borders(cell, color="dee2e6", sz="4"):
    tc   = cell._tc
    tcPr = tc.get_or_add_tcPr()
    tcBorders = OxmlElement("w:tcBorders")
    for side in ("top", "left", "bottom", "right"):
        el = OxmlElement(f"w:{side}")
        el.set(qn("w:val"),   "single")
        el.set(qn("w:sz"),    sz)
        el.set(qn("w:space"), "0")
        el.set(qn("w:color"), color.lstrip("#"))
        tcBorders.append(el)
    tcPr.append(tcBorders)

def para_in_cell(cell, text, bold=False, italic=False, font_size=9,
                 color=None, code=False, align=WD_ALIGN_PARAGRAPH.LEFT):
    cell.paragraphs[0].clear()
    p   = cell.paragraphs[0]
    p.alignment = align
    run = p.add_run(text)
    run.bold   = bold
    run.italic = italic
    run.font.size = Pt(font_size)
    if code:
        run.font.name = "Courier New"
    if color:
        r, g, b = hex_to_rgb(color)
        run.font.color.rgb = RGBColor(r, g, b)
    return p

# ── Document-level style helpers ─────────────────────────────────────────────
def add_heading(doc, text, level=1):
    p = doc.add_heading(text, level=level)
    p.alignment = WD_ALIGN_PARAGRAPH.LEFT
    return p

def add_body(doc, text):
    p = doc.add_paragraph(text)
    p.style = doc.styles["Normal"]
    p.paragraph_format.space_after = Pt(6)
    return p

def add_bullet(doc, text):
    p = doc.add_paragraph(style="List Bullet")
    # Support simple bold markers **text**
    parts = text.split("**")
    for i, part in enumerate(parts):
        run = p.add_run(part)
        run.bold = (i % 2 == 1)
    p.paragraph_format.space_after = Pt(3)
    return p

def add_code(doc, text):
    p = doc.add_paragraph()
    run = p.add_run(text)
    run.font.name = "Courier New"
    run.font.size = Pt(8)
    p.paragraph_format.left_indent = Cm(0.5)
    p.paragraph_format.space_after = Pt(4)
    return p

def add_caption(doc, text):
    p = doc.add_paragraph(text)
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = p.runs[0] if p.runs else p.add_run(text)
    run.italic = True
    run.font.size = Pt(8)
    p.paragraph_format.space_after = Pt(8)
    return p

def add_hr(doc):
    p = doc.add_paragraph()
    pPr = p._p.get_or_add_pPr()
    pBdr = OxmlElement("w:pBdr")
    bottom = OxmlElement("w:bottom")
    bottom.set(qn("w:val"),   "single")
    bottom.set(qn("w:sz"),    "6")
    bottom.set(qn("w:space"), "1")
    bottom.set(qn("w:color"), "1a1a2e")
    pBdr.append(bottom)
    pPr.append(pBdr)
    p.paragraph_format.space_after = Pt(4)

def add_image(doc, img_prefix, caption, width=Inches(6.0)):
    # Files are named like 01_role_hierarchy.png — match by numeric prefix
    import glob
    matches = sorted(glob.glob(os.path.join(IMG_DIR, f"{img_prefix}_*.png")))
    if not matches:
        # Fallback: exact name
        matches = [os.path.join(IMG_DIR, f"{img_prefix}.png")]
    path = matches[0]
    if os.path.exists(path):
        p = doc.add_paragraph()
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        run = p.add_run()
        run.add_picture(path, width=width)
    else:
        doc.add_paragraph(f"[Diagram not found: {img_prefix}]")
    add_caption(doc, caption)

def add_image_path(doc, path, caption, width=Inches(6.0)):
    """Embed an image from a full file path."""
    if os.path.exists(path):
        p = doc.add_paragraph()
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        run = p.add_run()
        run.add_picture(path, width=width)
    else:
        doc.add_paragraph(f"[Image not found: {os.path.basename(path)}]")
    add_caption(doc, caption)

# ── Generic table builder ─────────────────────────────────────────────────────
def add_table(doc, data, col_widths_cm, hdr_bg=HDR_BG, row1_bg=ROW1_BG):
    """data[0] = header row, data[1:] = body rows."""
    t = doc.add_table(rows=len(data), cols=len(data[0]))
    t.alignment = WD_TABLE_ALIGNMENT.LEFT
    t.style = "Table Grid"

    for r_idx, row_data in enumerate(data):
        row = t.rows[r_idx]
        is_hdr = (r_idx == 0)
        bg = hdr_bg if is_hdr else (row1_bg if r_idx % 2 == 1 else ROW2_BG)

        for c_idx, cell_text in enumerate(row_data):
            cell = row.cells[c_idx]
            cell.vertical_alignment = WD_ALIGN_VERTICAL.CENTER

            # Width
            if c_idx < len(col_widths_cm):
                cell.width = Cm(col_widths_cm[c_idx])

            set_cell_bg(cell, bg)
            set_cell_borders(cell)

            text_color = HDR_FG if is_hdr else None
            bold       = is_hdr
            code       = False
            para_in_cell(cell, str(cell_text), bold=bold,
                         font_size=8 if is_hdr else 8,
                         color=text_color, code=code)

    doc.add_paragraph()  # spacing after table
    return t


